package typesofloop;

public class WhileLoop {
	
	public static void main(String[] args) {
		
		// Write numbers from 1 to 10
		
		int i=1;
		while (i<=10)
		{
			System.out.println(i);
			i++;
		}
		

		// do-while loop
		// Write numbers from 1 to 10;
		
		
		int j=1;
		do
		{
			System.out.println(j);
			j++;
		}
		while(j<=10);
	}

}
